                <h2> Club Infos</h2>
            </div>
            <div class="row">
                <p> The logistics chain club was established in 2007 by a ground of logistics Students. Being a very active club for many years, the club was inactive for 3 years until Patrick Franck and Karamveer Singh braught the club back on Campus. Today, the club counts 110 members from around 26 countries. <br><br>
                The club is organized by 8 Coordinators: 4 Undergraduate students and 4 Graduate students. One of the Coordinators takes the role of President, who is the main contact person for Jacobs Univesity administration and Off-campus contact. The Club organizes every several events which includes the following.
                
                </p>
                <ul>
                    <li> Excel Training</li>
                    <li> Excursions to companies around Bremen like Mercedes, Arcelor Mittal, Airbus, BLG, DMK, Mars Pet food</li>
                    <li> Social Events / BBQ</li>
                    <li> Support during the studies</li>
                
